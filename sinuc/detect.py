import numpy as np


def detect(df, window=None, n_sigma=2.5):
    """
    Статистический детектор аномалий на основе скользящего окна и Z-score.

    Для каждой точки вычисляется локальное среднее и стандартное отклонение
    в окрестности (окно вокруг точки). Если нормализованное отклонение точки
    от локального среднего превышает порог n_sigma, точка помечается как аномалия.

    df : pandas.DataFrame
        DataFrame, содержащий колонку 'y' с значениями временного ряда.
    window : int | None
        Размер локального окна (в точках). Если None, выбирается автоматически
        как max(20, n // 100), где n — длина ряда.
    n_sigma : float
        Порог по Z-score. Чем больше значение, тем меньше точек будет признано
        аномальными (консервативнее детектор).
    """
    # Извлечение значений ряда в NumPy-массив для быстрой обработки
    y = df["y"].to_numpy()
    n = len(y)

    # Автоматический подбор размера окна, если не задан явно
    if window is None:
        window = max(20, n // 100)

    # Массив для хранения результатов детекции
    anomalies = np.zeros(n, dtype=bool)

    # Проход по каждой точке ряда
    for i in range(n):
        # Границы локального окна вокруг текущей точки
        start = max(0, i - window // 2)
        end = min(n, i + window // 2 + 1)

        # Локальная выборка
        local = y[start:end]

        # Локальные статистики
        local_avg = np.mean(local)
        local_std = np.std(local)

        # Проверка Z-score только если стандартное отклонение положительно
        # (защита от деления на ноль на вырожденных участках)
        if local_std > 0:
            z_score = abs(y[i] - local_avg) / local_std
            if z_score > n_sigma:
                anomalies[i] = True

    return anomalies